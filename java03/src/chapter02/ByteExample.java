package chapter02;

public class ByteExample {
	
	public static void main(String[] args) {
		
		int val1 = -129;
		byte val2 = -128;
		byte val3 = 0;
		byte val4 = 30;
		byte val5 = 127;
		
		
		System.out.println(val1);
		System.out.println(val2);
		System.out.println(val3);
		System.out.println(val4);
		System.out.println(val5);
		
	}
}
